export function AdminSidebar({ unreadCount, pendingRequestsCount, isOpen, onClose }: AdminSidebarProps) {
  const pathname = usePathname();
  const { t } = useLocale();

  const isActive = (href: string) => {
    if (href === "/admin") return pathname === "/admin";
    return pathname.startsWith(href);
  };

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 z-40 bg-black/30 lg:hidden"
          onClick={onClose}
          aria-hidden="true"
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          fixed inset-y-0 left-0 z-50 flex w-64 flex-col border-r border-neutral-200 bg-white
          transition-transform duration-200 ease-in-out
          lg:static lg:translate-x-0
          ${isOpen ? "translate-x-0" : "-translate-x-full"}
        `}
      >
        {/* Logo */}
        <div className="flex h-16 items-center justify-between border-b border-neutral-200 px-6">
          <Link href="/admin" className="text-lg font-semibold tracking-tight text-neutral-900">
            3DPrintIt
          </Link>
          <button
            type="button"
            onClick={onClose}
            className="rounded-md p-1 text-neutral-400 hover:bg-neutral-100 hover:text-neutral-600 lg:hidden"
            aria-label="Close sidebar"
          >
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto px-3 py-4 space-y-4">
          {navGroups.map((group, gi) => (
            <div key={gi}>
              {group.label && (
                <p className="mb-1 px-3 text-[10px] font-semibold uppercase tracking-widest text-neutral-400">
                  {group.label}
                </p>
              )}
              <ul className="space-y-0.5">
                {group.items.map((item) => {
                  const active = isActive(item.href);
                  return (
                    <li key={item.key}>
                      <Link
                        href={item.href}
                        onClick={onClose}
                        className={`
                          flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors
                          ${
                            active
                              ? "bg-neutral-900 text-white"
                              : "text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900"
                          }
                        `}
                      >
                        <NavIcon name={item.icon} />
                        <span className="flex-1">{t(item.key)}</span>
                        {item.key === "notifications" && unreadCount > 0 && (
                          <span
                            className={`
                              inline-flex h-5 min-w-[20px] items-center justify-center rounded-full px-1.5 text-xs font-semibold
                              ${active ? "bg-white text-neutral-900" : "bg-neutral-900 text-white"}
                            `}
                          >
                            {unreadCount > 99 ? "99+" : unreadCount}
                          </span>
                        )}
                        {item.key === "requests" && pendingRequestsCount > 0 && (
                          <span
                            className={`
                              inline-flex h-5 min-w-[20px] items-center justify-center rounded-full px-1.5 text-xs font-semibold
                              ${active ? "bg-white text-blue-600" : "bg-blue-500 text-white"}
                            `}
                          >
                            {pendingRequestsCount > 99 ? "99+" : pendingRequestsCount}
                          </span>
                        )}
                      </Link>
                    </li>
                  );
                })}
              </ul>
            </div>
          ))}
        </nav>

        {/* Footer */}
        <div className="border-t border-neutral-200 p-3">
          <span className="block px-3 text-xs text-neutral-400">3DPrintIt Admin v1.0</span>
        </div>
      </aside>
    </>
  );
}
